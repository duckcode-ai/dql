#!/usr/bin/env node
// Generate a synthetic dbt project with N models, forming a deterministic
// DAG (layered fan-in/fan-out). Used by CI's 4,000-model stress test and
// by local perf work — see docs/contribute/testing.
//
// Usage: node gen-dbt-project.mjs --models 4000 --out /tmp/stress
import { mkdirSync, writeFileSync } from 'node:fs';
import { resolve, join } from 'node:path';

function parseArgs(argv) {
  const o = { models: 4000, out: null, layers: 8 };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--models') o.models = Number(argv[++i]);
    else if (a === '--out') o.out = argv[++i];
    else if (a === '--layers') o.layers = Number(argv[++i]);
  }
  if (!o.out) { console.error('--out is required'); process.exit(2); }
  return o;
}

const { models, out, layers } = parseArgs(process.argv.slice(2));
const root = resolve(out);
const projectName = `stress_${models}`;
mkdirSync(join(root, 'models'), { recursive: true });
mkdirSync(join(root, 'target'), { recursive: true });

writeFileSync(join(root, 'dbt_project.yml'), `name: ${projectName}
version: '1.0.0'
profile: stress
model-paths: ["models"]
`);

writeFileSync(join(root, 'dql.config.json'), JSON.stringify({
  project: projectName,
  dbt: { manifestPath: 'target/manifest.json' },
}, null, 2) + '\n');

// Layer 0 = sources. Each subsequent layer fans in from the previous.
const perLayer = Math.ceil(models / layers);
const nodes = {};
const sources = {};
let idx = 0;
for (let layer = 0; layer < layers; layer++) {
  mkdirSync(join(root, 'models', `layer_${layer}`), { recursive: true });
  for (let i = 0; i < perLayer && idx < models; i++, idx++) {
    const name = `m_${layer}_${i}`;
    const relPath = `models/layer_${layer}/${name}.sql`;
    let sql;
    const dependsOn = [];
    const refs = [];
    const nodeSources = [];
    if (layer === 0) {
      sql = `select * from {{ source('raw', 's_${i}') }}`;
      const srcId = `source.${projectName}.raw.s_${i}`;
      nodeSources.push(['raw', `s_${i}`]);
      if (!sources[srcId]) {
        sources[srcId] = {
          resource_type: 'source',
          unique_id: srcId,
          name: `s_${i}`,
          source_name: 'raw',
          package_name: projectName,
          schema: 'raw',
          database: 'stress',
        };
      }
      dependsOn.push(srcId);
    } else {
      const a = i % perLayer;
      const b = (i + 1) % perLayer;
      sql = `select x.*
from {{ ref('m_${layer - 1}_${a}') }} x
join {{ ref('m_${layer - 1}_${b}') }} y using (id)`;
      refs.push([`m_${layer - 1}_${a}`], [`m_${layer - 1}_${b}`]);
      dependsOn.push(`model.${projectName}.m_${layer - 1}_${a}`);
      dependsOn.push(`model.${projectName}.m_${layer - 1}_${b}`);
    }
    writeFileSync(join(root, relPath), sql + '\n');
    const uniqueId = `model.${projectName}.${name}`;
    nodes[uniqueId] = {
      resource_type: 'model',
      unique_id: uniqueId,
      name,
      package_name: projectName,
      path: `layer_${layer}/${name}.sql`,
      original_file_path: relPath,
      schema: 'stress',
      database: 'stress',
      alias: name,
      depends_on: { nodes: dependsOn },
      refs,
      sources: nodeSources,
      raw_code: sql,
    };
  }
}

writeFileSync(join(root, 'target', 'manifest.json'), JSON.stringify({
  metadata: {
    dbt_schema_version: 'https://schemas.getdbt.com/dbt/manifest/v12.json',
    dbt_version: '1.9.0',
    project_name: projectName,
    generated_at: new Date().toISOString(),
  },
  nodes,
  sources,
  metrics: {},
  semantic_models: {},
  exposures: {},
}));

writeFileSync(join(root, 'README.md'), `# ${projectName}

Synthetic dbt project: ${models} models across ${layers} layers.
Generated by scripts/bench/gen-dbt-project.mjs — do not edit by hand.
`);

console.log(`✓ generated ${idx} models across ${layers} layers at ${root}`);
