#!/usr/bin/env node
// Generate a synthetic dbt project with N models, forming a deterministic
// DAG (layered fan-in/fan-out). Used by CI's 4,000-model stress test and
// by local perf work — see docs/contribute/testing.
//
// Usage: node gen-dbt-project.mjs --models 4000 --out /tmp/stress
import { mkdirSync, writeFileSync } from 'node:fs';
import { resolve, join } from 'node:path';

function parseArgs(argv) {
  const o = { models: 4000, out: null, layers: 8 };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--models') o.models = Number(argv[++i]);
    else if (a === '--out') o.out = argv[++i];
    else if (a === '--layers') o.layers = Number(argv[++i]);
  }
  if (!o.out) { console.error('--out is required'); process.exit(2); }
  return o;
}

const { models, out, layers } = parseArgs(process.argv.slice(2));
const root = resolve(out);
mkdirSync(join(root, 'models'), { recursive: true });

writeFileSync(join(root, 'dbt_project.yml'), `name: stress_${models}
version: '1.0.0'
profile: stress
model-paths: ["models"]
`);

// Layer 0 = sources. Each subsequent layer fans in from the previous.
const perLayer = Math.ceil(models / layers);
let idx = 0;
for (let layer = 0; layer < layers; layer++) {
  mkdirSync(join(root, 'models', `layer_${layer}`), { recursive: true });
  for (let i = 0; i < perLayer && idx < models; i++, idx++) {
    const name = `m_${layer}_${i}`;
    let sql;
    if (layer === 0) {
      sql = `select * from {{ source('raw', 's_${i}') }}`;
    } else {
      // Each model fans in from 2 parents in the previous layer — keeps the
      // graph realistic without exploding edge count.
      const a = i % perLayer;
      const b = (i + 1) % perLayer;
      sql = `select x.*
from {{ ref('m_${layer - 1}_${a}') }} x
join {{ ref('m_${layer - 1}_${b}') }} y using (id)`;
    }
    writeFileSync(join(root, 'models', `layer_${layer}`, `${name}.sql`), sql + '\n');
  }
}

writeFileSync(join(root, 'README.md'), `# stress_${models}

Synthetic dbt project: ${models} models across ${layers} layers.
Generated by scripts/bench/gen-dbt-project.mjs — do not edit by hand.
`);

console.log(`✓ generated ${idx} models across ${layers} layers at ${root}`);
